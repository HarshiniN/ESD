	DCD 0x10000000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=SRC;LOAD THE ADDRESS OF SRC TO RO
	LDR R1,=DST;LOAD THE ADDRESS OF SRC TO R1
	LDR R3,[R0];LOAD THE VALUE STORED IN THE ADDRESS STORED BY R0 INTO R3 
	STR R3,[R1];STORE THE VALUE OR R3 IN THE ADDRESS STORED BY R1
	ADD R0,#04;SHIFT THE ADDRESS BY 4 PLACES
	ADD R1,#04
	LDR R3,[R0]
	STR R3,[R1]
	ADD R0,#04
	ADD R1,#04
	LDR R3,[R0]
	STR R3,[R1]
STOP B STOP
SRC DCD 0,0,0,0
	AREA DATASEG,DATA,READWRITE

DST DCD 0,0,0
	END